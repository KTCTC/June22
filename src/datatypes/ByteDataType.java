package datatypes;

public class ByteDataType {

	public static void main(String[] args) {
		
		byte a;
		byte b = 33;
		byte c = 90;
		
		a = 45;
		System.out.println(b);
		
		System.out.println(a);
		
		a = 55;
		
		System.out.println(a);
		System.out.println(b);
		
		c = b;
		System.out.println(c);
		
		

	}

}
