package operators;

public class RelationalOperators {

	public static void main(String[] args) {
		// Relational operator
		//   <,>,<=,>=,==,!=
		
		int a = 8;
		int b = 4;
		
		boolean h = a!=b;
		System.out.println(h);
		
		boolean g = a==b;
		System.out.println(g);
		boolean f = a>=b;
		System.out.println(f);
		boolean e = a<=b;
		System.out.println(e);
		
		boolean d = a>b;
		System.out.println(d);
		
		boolean c = a<b;
		System.out.println(c);

	}

}
